fun main() {
    /*
    \t – tab

    \b – backspace

    \n – new line (LF)

    \r – carriage return (CR)

    \' – single quotation mark

    \" – double quotation mark

    \\ – backslash

    \$ – dollar sign    */

    println("Abhi\tshek")
    println("Abhi\nshek")
    println("Abhi\'shek")
    println("Abhi\"shek")
    println("Abhi\\shek")
    println("Abhi\$shek")
    println("Abhi\bshek")   // Removes the 'i' before 's'
    println("Abhishek\rshek")   // Overwrites "First line" with "Second line"


    // multi line string


}